package com.syntax.class08;

public class ForLoopExamples {

	public static void main(String[] args) {
		// I need to print number from 1 to 90
		
		for (int i=1; i<=90; i++) {
			System.out.print(i+" ");
		}
		System.out.println("_______________1____________________________________");
		/*
		 * start point
		 * end point
		 * increment/decrement
		 * 
		 */
		// from 60 to 10
		for (int u=60; u>=10; u--) {
			System.out.print(u+" ");
		}
		System.out.println("_______________1____________________________________");
		// what is the output?
		for(int t=5; t<=40; t+=5) {
			System.out.print(t+" ");

		}



	}

}
