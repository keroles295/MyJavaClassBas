package com.syntax.class07;

public class PreincerementAndPostincrement {

	public static void main(String[] args) {
		// post increment
		//first use and then increment
		int nm=10;
		int res=nm++; // preincrement
		System.out.println("Value of nm = "+nm);
		System.out.println("Value of res = "+res);
		int nm1=10;
		
		int res1=++nm1;
		System.out.println("Value of nm1 = "+nm1);
		System.out.println("Value of res1 = "+res1);
		


	}

}
