package com.syntax.class07;

public class WhileLoop {

	public static void main(String[] args) {
		
		//while loop
		int time=1;
		if (time<12) {
			System.out.println("Morning"); //1 
		}
		System.out.println("_____________");
		
		while (time<12) {
			System.out.println("Good Morning"); // infinite 
			time++;
			// add variable ++;
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
